function captureDefaultTexts() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!defaultTexts.has(key)) {
      defaultTexts.set(key, el.textContent.trim());
    }
  });
}

function translateCommonStaticText(lang) {
  const reverseMap = Object.fromEntries(
    Object.entries(commonStaticTextMap).map(([en, zh]) => [zh, en])
  );

  document.querySelectorAll('h2, h3, .side-title, .section-title, .proj-h2').forEach((el) => {
    if (el.hasAttribute('data-i18n')) return;

    if (!staticTextSnapshot.has(el)) {
      staticTextSnapshot.set(el, el.textContent.trim());
    }

    const original = staticTextSnapshot.get(el);
    if (lang === 'zh' && commonStaticTextMap[original]) {
      el.textContent = commonStaticTextMap[original];
      return;
    }

    if (lang === 'en') {
      const current = el.textContent.trim();
      if (reverseMap[current]) {
        el.textContent = reverseMap[current];
        return;
      }
      el.textContent = original;
    }
  });
}

function updateStaticNavLabels(lang) {
  const labels = {
    home: lang === 'zh' ? '\u9996\u9875' : 'Home',
    resume: lang === 'zh' ? '\u7b80\u5386' : 'Resume',
    linkedin: 'LinkedIn',
    photography: lang === 'zh' ? '\u6444\u5f71' : 'Photography',
    architecture: lang === 'zh' ? '\u5efa\u7b51' : 'Architecture',
  };

  document.querySelectorAll('.nav-link').forEach((link) => {
    const href = link.getAttribute('href') || '';
    if (href.endsWith('index.html') || href === '../index.html') {
      link.textContent = labels.home;
      return;
    }
    if (href.endsWith('resume.html') || href === '../resume.html') {
      link.textContent = labels.resume;
      return;
    }
    if (href.includes('linkedin.com')) {
      link.textContent = labels.linkedin;
      return;
    }
    if (href.endsWith('photography.html') || href === '../photography.html') {
      link.textContent = labels.photography;
      return;
    }
    if (href.endsWith('architecture-portfolio.html') || href === '../projects/architecture-portfolio.html') {
      link.textContent = labels.architecture;
    }
  });
}

function updateLangButtonLabels() {
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    const lang = btn.getAttribute('data-lang');
    if (lang === 'en') btn.textContent = 'EN';
    if (lang === 'zh') btn.textContent = '\u4e2d';
  });
}

function getTextForKey(lang, key) {
  if (lang === 'zh' && zhTranslations[key] !== undefined) {
    return zhTranslations[key];
  }
  if (lang === 'zh' && key === 'hero_meta_location') {
    return '';
  }
  return defaultTexts.get(key) || '';
}

function setLanguage(lang) {
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const text = getTextForKey(lang, key);
    if (text) {
      el.textContent = text;
    }
  });

  updateStaticNavLabels(lang);
  updateLangButtonLabels();
  translateCommonStaticText(lang);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  localStorage.setItem('cv_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  captureDefaultTexts();
  updateLangButtonLabels();

  const saved = localStorage.getItem('cv_lang') || 'zh';
  setLanguage(saved);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
});
